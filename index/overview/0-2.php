<p>Краткая информация об игре &mdash; мультиплеерному экшену, выросшему из мода для Unreal Tournament 2004Краткая информация об игре. Через три года она решила повторить этот фокус с Killing Floor &mdash; &laquo;кооперативной&raquo; модификацией для той же UT 2004. Краткая информация об игре, в которой шестеро бойцов защищают Великобританию от орд монстров. Минимум предыстории, чуть-чуть &laquo;командного взаимодействия&raquo; и максимум стрельбы &mdash; не худшие задатки, будь авторы порасторопнее. Выпускать подобную игру после Left 4 Dead. Краткая информация об игре &mdash; безумие.</p>

<p>Создатели &laquo;ужастиков&raquo; о нашествии бродячих мертвецов/мутантов/жертв вируса редко тратят ресурсы на вдумчивую проработку мира. Кто обратит на нее внимание, когда вокруг гремят дробовики, а упыри громко чавкают мозгами? Гораздо важнее ядреный сценарий, оригинальные декорации и ведра кетчупа. В &laquo;киношных&raquo; кампаниях Left 4 DeadКраткая информация об игре вообще нет сюжета, но по накалу страстей и атмосфере они не уступают лучшим &laquo;сингловым&raquo; шутерам.</p>

<p>У Tripwire все шиворот-навыворот. По меркам жанра, информации о случившейся катастрофе немало. Корпорация Horzine Biotech выращивала в бункере под Лондоном искусственных суперсолдат. К несчастью, процесс клонирования вышел из-под контроля. Поток зомби затопил столицу, растекся по окрестностям, так что правительству пришлось стянуть уцелевшие подразделения полиции и армии. Увы, кроме завязки, фантазии ни на что не хватило.</p>

<p>В L4DКраткая информация об игре необходимо было постоянно двигаться, чтобы не погибнуть под натиском &laquo;зараженных&raquo;. Пробежки по линейным локациям превращались в настоящее приключение. Killing Floor пляшет от другой печки. Ее карты похожи на арены для сетевых схваток из UT 2004Краткая информация об игре &mdash; сплетения коридоров либо участки местности на свежем воздухе. Игроки занимают позицию для обороны и истребляют толпы уродов, получая за их скальпы денежное вознаграждение. Когда они перемелют очередную порцию &laquo;мяса&raquo;, откроется магазин, торгующий &laquo;пушками&raquo; и боеприпасами. Орудий убийства &mdash; с избытком, хотя проку от экзотики вроде арбалетов, двустволок и огнеметов немного.</p>

<p>После короткой передышки подступает новая &laquo;волна&raquo;, приносящая более тучные стада монстров. К обычным зомби присоединяются фрики с бензопилами, толстяки с несварением желудка, ползающие на четвереньках &laquo;прыгуны&raquo;, невидимки и &laquo;сирены&raquo;, оглушающие ультразвуковыми песнями. В финале на сцену выползает босс, вооруженный многоствольным пулеметом. Зверинец богат, вот только искать подход к каждому противнику, как в L4DКраткая информация об игре, не нужно. Самый мудреный тактический маневр &mdash; знакомое по Alien ShooterКраткая информация об игре отступление спиной вперед. Зачастую все еще проще &mdash; если команда забралась в середину длинного прохода и ощетинилась стволами в обе стороны, она практически неуязвима.</p>

<p>Непонятно, на кого рассчитаны первый и второй из четырех уровней сложности &mdash; и без того вялая забава становится избиением младенцев. Точно в консольной Battlefield: Bad Company. Краткая информация об игре, каждый боец наделен &laquo;вечным&raquo; шприцем для лечения. Стоит залпам стихнуть на пару секунд, и раны исчезают. Ни чувства опасности, ни азарта. Пока члены отряда ведут себя не по-идиотски, проиграть невозможно. Режим Survival, появившийся в Left 4 DeadКраткая информация об игре с апрельским обновлением, предлагает схожий геймплей, но там не заскучаешь &mdash; беспрерывные атаки &laquo;специальных зараженных&raquo; не дают передохнуть.</p>

